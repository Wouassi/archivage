<?php

namespace App\Models;

use Filament\Models\Contracts\FilamentUser;
use Filament\Panel;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Spatie\Permission\Traits\HasRoles;

class User extends Authenticatable implements FilamentUser
{
    use HasFactory, Notifiable, HasRoles;

    protected $fillable = ['name', 'email', 'password', 'active'];
    protected $hidden = ['password', 'remember_token'];

    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
            'active' => 'boolean',
        ];
    }

    public function canAccessPanel(Panel $panel): bool
    {
        return $this->active ?? true;
    }

    public function dossiers()
    {
        return $this->hasMany(Dossier::class, 'created_by');
    }

    public function activityLogs()
    {
        return $this->hasMany(ActivityLog::class);
    }

    public function hasMinimumRole(string $role): bool
    {
        $hierarchy = ['AssociÃ©' => 1, 'Expert-comptable' => 2, 'Collaborateur' => 3, 'Assistant' => 4];
        $userLevel = 99;
        foreach ($hierarchy as $roleName => $level) {
            if ($this->hasRole($roleName)) {
                $userLevel = min($userLevel, $level);
            }
        }
        return $userLevel <= ($hierarchy[$role] ?? 99);
    }
}
